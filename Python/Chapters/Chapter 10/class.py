class Employee:
    language = "Python"
    salary = 11000000
ritul = Employee()
ritul.name = "Ritul"
print(ritul.language, ritul.salary, ritul.name)
rohan = Employee()
rohan.name = "Rohan" 
print(rohan.language, rohan.salary, rohan.name)
# Here name is instance attribute and salary and name are class attributes as they belong
# to the class.
